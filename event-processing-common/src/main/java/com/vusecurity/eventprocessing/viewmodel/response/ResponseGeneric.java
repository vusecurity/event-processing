package com.vusecurity.eventprocessing.viewmodel.response;

import java.util.HashMap;
import java.util.Map;

public class ResponseGeneric {

    private int code;
    private String message;
    private Map<String, Object> data = new HashMap<String, Object>();

    public ResponseGeneric() {
    }

    public ResponseGeneric(int code, String message) {
        this.code = code;
        this.message = message;
    }

    public ResponseGeneric(int code, String message, Map<String, Object> data) {
        this.code = code;
        this.message = message;
        this.data = data;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public Map<String, Object> getData() {
        return data;
    }

    public void setData(Map<String, Object> data) {
        this.data = data;
    }

    public void addData(String string, Object object) {
        this.data.put(string, object);
    }
}

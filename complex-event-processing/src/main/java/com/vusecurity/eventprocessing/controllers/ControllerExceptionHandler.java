package com.vusecurity.eventprocessing.controllers;

import com.vusecurity.eventprocessing.services.exceptions.StatementCompileException;
import com.vusecurity.eventprocessing.viewmodel.response.BaseResponse;
import org.slf4j.MDC;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.ControllerAdvice;
import org.springframework.web.bind.annotation.ExceptionHandler;
import org.springframework.web.bind.annotation.ResponseBody;

@ControllerAdvice
public class ControllerExceptionHandler {

    @ResponseBody
    @ExceptionHandler(StatementCompileException.class)
    public ResponseEntity<BaseResponse<Void>> compilationException(RuntimeException ex){

        BaseResponse<Void> response = new BaseResponse<>(10020, ex.getCause().getMessage(), MDC.get("traceId"));

        return new ResponseEntity<>(response, HttpStatus.BAD_REQUEST);
    }
}

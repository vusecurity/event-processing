package com.vusecurity.eventprocessing.controllers;

import com.vusecurity.eventprocessing.viewmodel.response.ResponseGeneric;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RestController;

@RestController
public class MainController {

    @Value("${application.version}")
    private String version;

    @GetMapping(value = "/version")
    public ResponseEntity<ResponseGeneric> version(){
        ResponseGeneric response;
        HttpStatus status;

        response = new ResponseGeneric(0000, "Complex event processing engine");
        response.addData("version", version);
        status = HttpStatus.OK;

        return new ResponseEntity<>(response, status);
    }
}
